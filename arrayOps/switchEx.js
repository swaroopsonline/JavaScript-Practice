
function switchEx(num){


    switch (num) {
        case 1:            
        console.log("one")
            break;

        case 2:            
        console.log("two")
            break;

        case 3:            
        console.log("three")
            break;

        case 4:            
        console.log("Four")
            break;    
    
        default:
            break;

}

}

switchEx(3)